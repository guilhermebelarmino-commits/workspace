--- TABLA PRIORIZACION ---
CREATE OR REPLACE TEMP TABLE TMP_PRIORIZACION AS

    SELECT 
        A.*,
        TMP_CONVICENCIA.CC CONV_CC,
        TMP_CONVICENCIA.TC CONV_TC,
        TMP_PARAMETRIA.PRIORIZACION,
        TMP_PARAMETRIA.PRIORIZACION_DETALLE,
        TMP_PARAMETRIA.PORC_CC PORC_CC,
        TMP_PARAMETRIA.PORC_TC PORC_TC,
        TMP_TOPE.NIVEL_RIESGO NIVEL_RIESGO_CONJUNTO,
        TMP_TOPE.RCI_TOPE TOPE_CONJUNTO,

        --- SI EL USUARIO ES ACT, EL TOPE DE CC ES 0 Y EL TOPE DE TC ES EL MAX RCI CONJUNTA
        CASE 
            WHEN FLAG_REPEAT = TRUE THEN TMP_TOPE.RCI_TOPE * TMP_PARAMETRIA.PORC_CC
            ELSE 0
        END AS TOPE_CC,
        CASE 
            WHEN FLAG_REPEAT = TRUE THEN TMP_TOPE.RCI_TOPE * TMP_PARAMETRIA.PORC_TC
            ELSE TMP_TOPE.RCI_TOPE
        END AS TOPE_TC,

        MULTI_MAX MULTIPLICADOR_NOMINAL_CONJUNTO,
        LIMITE_TOTAL * MULTI_MAX LIMITE_NOMINAL_CONJUNTO,
        ((LIMITE_TOTAL * MULTI_MAX) - LIMITE_TOTAL) AS AUMENTO_NOMINAL_CONJUNTO,


        --- AUMENTO NOMINAL TC: SI EL USUARIO ES REPEAT: AUMENTO NOMINAL CONJUNTO * PORC TC REPEAT // SI EL USUARIO ES ACT: AUMENTO NOMINAL CONJUNTO
        CASE 
            WHEN FLAG_REPEAT = TRUE THEN ((LIMITE_TOTAL * MULTI_MAX) - LIMITE_TOTAL) * TMP_PARAMETRIA.PORC_TC
            ELSE ((LIMITE_TOTAL * MULTI_MAX) - LIMITE_TOTAL)
        END AS AUMENTO_NOMINAL_TC,

        FROM TMP_TABLA_TC A
        LEFT JOIN TMP_CONVICENCIA 
