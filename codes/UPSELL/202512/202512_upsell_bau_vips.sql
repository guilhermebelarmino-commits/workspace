--- TABLA LIMITES ---

CREATE OR REPLACE TEMP TABLE TMP_LIMITE AS

    SELECT
        A.*,
        CASE 
            WHEN ROUND (LEAST (LIMITE_MULTIPLICADOR, LIMITE_RCI, LIMITE_NOMINAL_TC), -2)  > LIMITE_NOMINAL_TC_INTERNO THEN LIMITE_NOMINAL_TC_INTERNO
            ELSE ROUND (LEAST (LIMITE_MULTIPLICADOR, LIMITE_RCI, LIMITE_NOMINAL_TC), -2) 
        END AS LIMITE_FINAL,

        CASE 
            WHEN ROUND (LEAST (LIMITE_MULTIPLICADOR, LIMITE_RCI, LIMITE_NOMINAL_TC), -2) > LIMITE_NOMINAL_TC THEN 'limite_nominal_tc'
            ELSE
                CASE 
                    WHEN ROUND (LEAST (LIMITE_MULTIPLICADOR, LIMITE_RCI, LIMITE_NOMINAL_TC), -2) = ROUND (LIMITE_MULTIPLICADOR, -2) THEN 'limite_multiplicador'
                    WHEN ROUND (LEAST (LIMITE_MULTIPLICADOR, LIMITE_RCI, LIMITE_NOMINAL_TC), -2) = ROUND (LIMITE_RCI,-2) THEN 'limite_rci'
                    WHEN ROUND (LEAST (LIMITE_MULTIPLICADOR, LIMITE_RCI, LIMITE_NOMINAL_TC), -2) = ROUND (LIMITE_NOMINAL_TC,-2) THEN 'limite_nominal_tc'
                    ELSE 'otro_caso' 
                END
        END AS ORIGEN_LIMITE_FINAL
    FROM TMP_TOPES A
    WHERE 1=1
    AND FLAG_TOPE_CONJ_RCI IS FALSE
    AND FLAG_TOPE_TC_RCI IS FALSE
    AND KILLER_AUTOFONDEO IS TRUE;

